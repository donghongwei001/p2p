package com.entity;

public class Outtime {
	String projectname;
	int outday;
	float outmoney;
	public String getProjectname() {
		return projectname;
	}
	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}
	public int getOutday() {
		return outday;
	}
	public void setOutday(int outday) {
		this.outday = outday;
	}
	public float getOutmoney() {
		return outmoney;
	}
	public void setOutmoney(float outmoney) {
		this.outmoney = outmoney;
	}
	
}
